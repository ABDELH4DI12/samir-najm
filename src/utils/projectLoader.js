export const getCategoriesFromProjects = (projects) => {
  const categories = [
    { id: 'all', name: 'All Work', count: projects.length }
  ];
  
  const categoryMap = {};
  
  projects.forEach(project => {
    if (!categoryMap[project.category]) {
      categoryMap[project.category] = {
        id: project.category,
        name: project.category.charAt(0).toUpperCase() + project.category.slice(1),
        count: 0
      };
    }
    categoryMap[project.category].count++;
  });
  
  return [...categories, ...Object.values(categoryMap)];
};
